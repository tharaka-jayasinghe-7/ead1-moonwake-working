    private void btn_addREActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addREActionPerformed
       // Validate date inputs
    Date checkinDate = date_checkInRE.getDate();
    Date checkoutDate = date_checkOutRE.getDate();

    if (checkinDate == null || checkoutDate == null) {
        JOptionPane.showMessageDialog(this, "Please enter the values.", "Validation Error", JOptionPane.ERROR_MESSAGE);
    } else {
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String dateString1 = dateFormat.format(checkinDate);
        String dateString2 = dateFormat.format(checkoutDate);

    // Validate amount input
    String amountText = txt_amount1RE.getText();
    if (amountText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter the amount.", "Validation Error", JOptionPane.ERROR_MESSAGE);
    } else {
        try {
            int amount = Integer.parseInt(amountText);

            // Check if amount is a positive integer
            if (amount <= 0) {
                JOptionPane.showMessageDialog(this, "Amount must be a positive integer.", "Validation Error", JOptionPane.ERROR_MESSAGE);
            } else {
                // If all validations pass, proceed with adding the reservation
                reController1.addReservation(lbl_reID.getText(), txt_nicRE.getText(), cmb_roomNORE.getSelectedItem().toString(), dateString1, dateString2, amount);
                ReservationModel reModel1 = new ReservationModel();
                reController1.autoID(reModel1, lbl_reID);

                DefaultTableModel model = (DefaultTableModel) tbl_reservation.getModel();
                model.setRowCount(0);

                reController1.loadReservation(tbl_reservation, reModel1);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid amount.", "Validation Error", JOptionPane.ERROR_MESSAGE);
        }
    }
}
    
    }//GEN-LAST:event_btn_addREActionPerformed

