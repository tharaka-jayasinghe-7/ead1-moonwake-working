    private void calculateAmount(){
        if(date_checkInRE.getDate() != null && date_checkOutRE.getDate() != null){
            LocalDate start = date_checkInRE.getDate().toInstant()
            .atZone(ZoneId.systemDefault())
            .toLocalDate();
                  LocalDate end   = date_checkOutRE.getDate().toInstant()
            .atZone(ZoneId.systemDefault())
            .toLocalDate();

            long diffInDays = ChronoUnit.DAYS.between(start, end);
            
            Iterator it = roomsRE.iterator();
            while (it.hasNext()){
                RoomModel rModel = (RoomModel)it.next();
                if(cmb_roomNORE.getSelectedItem() != null){
                if(rModel.getRoom_id().equals(cmb_roomNORE.getSelectedItem().toString())){
                    txt_amount1RE.setText(String.valueOf(diffInDays * rModel.getRoom_amount()));
                }
                }
            }
        }
        
    }
    
    private void initializeTimer() {
    // Create a timer to update the label every second (1000 milliseconds)
    Timer timer = new Timer(1000, new ActionListener() {
        public void actionPerformed(ActionEvent e) {
            // Get the current date and time
            Date currentDate = new Date();

            // Define a date format
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");

            // Format the date as a string
            String formattedDate = dateFormat.format(currentDate);

            // Set the label's text to the formatted date and time
            lbl_SysDate.setText(formattedDate);
        }
    });

    // Start the timer
    timer.start();
    }
