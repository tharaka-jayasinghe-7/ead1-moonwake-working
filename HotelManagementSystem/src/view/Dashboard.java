    private void loadRoomsRE(){
        try {
            cmb_roomNORE.removeAllItems();
            if(cmb_bedTypeRE.getSelectedItem() != null && cmb_roomTypeRE.getSelectedItem() != null){
                String bedType = cmb_bedTypeRE.getSelectedItem().toString();
                String roomType = cmb_roomTypeRE.getSelectedItem().toString();

                roomsRE = roomController1.getRoomsByType(roomType, bedType);

                Iterator it = roomsRE.iterator();
                while (it.hasNext()){
                    RoomModel rModel = (RoomModel)it.next();
                    cmb_roomNORE.addItem(rModel.getRoom_id());
                }
            }
            
        } catch (Exception e) {
            System.out.println(e.toString());
        }
    }
    
