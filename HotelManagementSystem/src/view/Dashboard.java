    private void btn_addCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addCustomerActionPerformed

        CustomerModel customerModel1 = new CustomerModel();
String nic = txt_nic.getText().trim();
String cusName = txt_cusName.getText().trim();
String cusAddress = txt_cusAddress.getText().trim();
String mobileText = txt_mobile.getText().trim();
String gender = cmb_gender.getSelectedItem().toString();
String email = txt_email.getText().trim();

// Validate input
if (nic.isEmpty() || cusName.isEmpty() || cusAddress.isEmpty() || mobileText.isEmpty() || email.isEmpty()) {
    // Display an error message if any required field is empty
    JOptionPane.showMessageDialog(this, "Please fill in all required fields.", "Validation Error", JOptionPane.ERROR_MESSAGE);
} else {
    try {
        // Check if mobileText is a valid integer
        int mobile = Integer.parseInt(mobileText);

        // If all validations pass, proceed with adding the customer
        customerModel1.setCustomer_nic(nic);
        customerModel1.setCustomer_name(cusName);
        customerModel1.setCustomer_adress(cusAddress);
        customerModel1.setCustomer_mobile(mobile);
        customerModel1.setCustomer_gender(gender);
        customerModel1.setCustomer_email(email);

        customerController1.addCustomer(customerModel1);

        DefaultTableModel model = (DefaultTableModel) tbl_customer.getModel();
        model.setRowCount(0);

        customerController1.loadCustomer(tbl_customer, customerModel1);
    } catch (NumberFormatException e) {
        // Display an error message if mobileText is not a valid integer
        JOptionPane.showMessageDialog(this, "Please enter a valid mobile number.", "Validation Error", JOptionPane.ERROR_MESSAGE);
    }
}

    }//GEN-LAST:event_btn_addCustomerActionPerformed

